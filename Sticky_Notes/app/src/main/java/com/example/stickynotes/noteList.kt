package com.example.stickynotes

class NotesList (private var id: String, private var title: String, private var note: String, ) {
    fun getID() : String? {
        return id
    }
    fun getTitle() : String? {
        return title
    }
    fun getNote() : String? {
        return id
    }
    fun setID() : String? {
        return note
    }
    fun setTitle() : String? {
        return title
    }
    fun setNote() : String? {
        return id
    }
}